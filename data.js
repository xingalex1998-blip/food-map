// ── 数据版本（修改此值可强制清除旧缓存）────────────────────
const DATA_VERSION = 'v10_real_desc';

const STORAGE_KEY = 'food_map_spots';

function loadSpots() {
  try {
    if (localStorage.getItem('food_map_version') !== DATA_VERSION) {
      localStorage.removeItem(STORAGE_KEY);
      localStorage.setItem('food_map_version', DATA_VERSION);
    }
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored) return JSON.parse(stored);
  } catch(e) {}
  const defaults = getDefaultSpots();
  localStorage.setItem(STORAGE_KEY, JSON.stringify(defaults));
  return defaults;
}
